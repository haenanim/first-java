package day2;

import java.util.Scanner;

public class SwitchEx {
  public static void main(String[] args) {
    Scanner in = new Scanner(System.in);
    int num = in.nextInt();
    switch (num) {
      case 3:
        System.out.print("*");

      case 2:

        System.out.print("*");

      case 1:

        System.out.print("*");

    }
  }
}
